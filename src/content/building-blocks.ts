export const TOTAL_STEPS = 100;

/**
 * Get the next step to learn based on completed steps.
 * Returns null if all steps are completed.
 */
export function getNextStep(completedSteps: number[]): BuildingBlock | null {
  for (const block of buildingBlocks) {
    if (!completedSteps.includes(block.step)) {
      return block;
    }
  }
  return null;
}

/**
 * Get a building block by its step number.
 */
export function getStepByNumber(step: number): BuildingBlock | undefined {
  return buildingBlocks.find((b) => b.step === step);
}

/**
 * Get the count of completed steps.
 */
export function getCompletedCount(completedSteps: number[]): number {
  return completedSteps.filter((s) => s >= 1 && s <= TOTAL_STEPS).length;
}

/**
 * Check if a step is unlocked (all previous steps are completed).
 * Step 1 is always unlocked.
 */
export function isStepUnlocked(
  step: number,
  completedSteps: number[]
): boolean {
  if (step === 1) return true;
  // A step is unlocked if all previous steps are completed
  for (let i = 1; i < step; i++) {
    if (!completedSteps.includes(i)) {
      return false;
    }
  }
  return true;
}

/**
 * Get the current phase based on completed steps.
 */
export function getCurrentPhase(completedSteps: number[]): 1 | 2 | 3 | 4 {
  const maxCompleted = completedSteps.length > 0 ? Math.max(...completedSteps) : 0;
  if (maxCompleted >= 76) return 4;
  if (maxCompleted >= 51) return 3;
  if (maxCompleted >= 26) return 2;
  return 1;
}

